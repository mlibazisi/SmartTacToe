:nerd_face: *Welcome to SmartTacToe!*
_A stateless, quasi-sentient, optimal-play predictive m,n,k-game for Slack_
    1. Use `/ttt help` to list the available commands
    2. Use `/ttt challenge @opponent` to play against an opponent
    3. Use `/ttt status` to view an active game
    4. Use `/ttt end` to end a game